#include "push_swap.h"

static void	ft_putchar(int c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
		write(1, "(null)", 6);
	while (s[i])
	{
		ft_putchar(s[i]);
		i++;
	}
}
